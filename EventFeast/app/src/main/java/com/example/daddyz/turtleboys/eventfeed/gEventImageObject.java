public class gEventImageObject {
    private String image_external_url;
    private String image_category;
    private String image_height;
    private String image_width;

    public String getImage_external_url() {
        return image_external_url;
    }

    public void setImage_external_url(String image_external_url) {
        this.image_external_url = image_external_url;
    }

    public String getImage_category() {
        return image_category;
    }

    public void setImage_category(String image_category) {
        this.image_category = image_category;
    }

    public String getImage_height() {
        return image_height;
    }

    public void setImage_height(String image_height) {
        this.image_height = image_height;
    }

    public String getImage_width() {
        return image_width;
    }

    public void setImage_width(String image_width) {
        this.image_width = image_width;
    }
}
